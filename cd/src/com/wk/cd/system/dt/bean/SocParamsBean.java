package com.wk.cd.system.dt.bean;

/**
 * Class Description: 数据源参数接口
 * @author xuph
 */
public class SocParamsBean {

	private String url;
	public static final String URLCN = "URL";

	private String ip;
	public static final String IPCN = "IP地址";

	private int port;
	public static final String PORTCN = "端口号";

	private String uname;
	public static final String UNAMECN = "目标主机用户名";

	private String passwd;
	public static final String PASSWDCN = "目标主机密码";

	/**
	 * @return url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return ip IP地址
	 */
	public String getIp() {
		return this.ip;
	}

	/**
	 * @param ip IP地址
	 */
	public void setIp(String ip) {
		this.ip = ip;
	}

	/**
	 * @return port 端口号
	 */
	public int getPort() {
		return this.port;
	}

	/**
	 * @param port 端口号
	 */
	public void setPort(int port) {
		this.port = port;
	}

	/**
	 * @return remote_uname 目标主机用户名
	 */
	public String getUname() {
		return this.uname;
	}

	/**
	 * @param uname 目标主机用户名
	 */
	public void setUname(String uname) {
		this.uname = uname;
	}

	/**
	 * @return remote_passwd 目标主机密码
	 */
	public String getPasswd() {
		return this.passwd;
	}

	/**
	 * @param passwd 目标主机密码
	 */
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
}
