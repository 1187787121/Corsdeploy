package com.wk.cd.system.us.bean;
import java.util.List;

/**
 * Class Description:
 * @author lixl
 */
public class MenuBean {
	private String name;
	
	private int sort_key;
	
	private String frame;

	private List<TabBean> tabs;

	private int defaulttab;
	
	/**
	 *资源图标地址
	 */
	private String rsim_url;

	public static final String RSIM_URLCN = "资源图标地址";

	/**
	 * @return name
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return tabs
	 */
	public List<TabBean> getTabs() {
		return this.tabs;
	}

	/**
	 * @param tabs
	 */
	public void setTabs(List<TabBean> tabs) {
		this.tabs = tabs;
	}

	/**
	 * @return defaulttab
	 */
	public int getDefaulttab() {
		return this.defaulttab;
	}

	/**
	 * @param defaulttab
	 */
	public void setDefaulttab(int defaulttab) {
		this.defaulttab = defaulttab;
	}
	
	public int getSort_key(){
		return this.sort_key;
	}
	
	public void setSort_key(int sort_key){
		this.sort_key = sort_key;
	}

	/**
	 * @return frame
	 */
	public String getFrame() {
		return this.frame;
	}

	/**
	 * @param frame
	 */
	public void setFrame(String frame) {
		this.frame = frame;
	}

	/**
	 * @return rsim_url 资源图标地址
	 */
	public String getRsim_url() {
		return rsim_url;
	}

	/**
	 * @param rsim_url 资源图标地址
	 */
	public void setRsim_url(String rsim_url) {
		this.rsim_url = rsim_url;
	}
}

